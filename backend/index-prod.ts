// Load encrypted secrets into process.env before any other module initialises.
// Requires SECRETS_KEY env var at runtime; falls back to plaintext .env in dev.
import '../scripts/secrets/load-secrets.mjs';

import fs from "node:fs";
import { type Server } from "node:http";
import path from "node:path";

import express, { type Express, type Request } from "express";

import runApp from "./app";

export async function serveStatic(app: Express, server: Server) {
  // When compiled to build/index.js, import.meta.dirname is the build/ directory.
  // Vite outputs the frontend to dist/ (project root), so we go one level up.
  const distPath = path.resolve(import.meta.dirname, "..", "dist");

  if (!fs.existsSync(distPath)) {
    throw new Error(
      `Could not find the build directory: ${distPath}, make sure to build the client first`,
    );
  }

  app.use(express.static(distPath));

  // fall through to index.html if the file doesn't exist
  app.use("*", (_req, res) => {
    res.sendFile(path.resolve(distPath, "index.html"));
  });
}

(async () => {
  await runApp(serveStatic);
})();
